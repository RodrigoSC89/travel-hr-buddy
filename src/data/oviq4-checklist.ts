export const getQuestionsForVesselType = (vesselType: string): OVIQQuestion[] => {
  const allQuestions: OVIQQuestion[] = [];
  
  OVIQ4_SECTIONS.forEach(section => {
    section.questions.forEach(question => {
      if (!question.vesselTypes || question.vesselTypes.includes(vesselType)) {
        allQuestions.push(question);
      }
    });
  });
  
  return allQuestions;
};

export const getTotalQuestions = (): number => {
  return OVIQ4_SECTIONS.reduce((total, section) => total + section.questions.length, 0);
};
