export const getTotalMLCItems = (): number => {
  return MLC_CATEGORIES.reduce((total, category) => total + category.items.length, 0);
};

export const getCriticalItems = (): MLCChecklistItem[] => {
  const criticalItems: MLCChecklistItem[] = [];
  MLC_CATEGORIES.forEach(category => {
    category.items.forEach(item => {
      if (item.critical) {
        criticalItems.push(item);
      }
    });
  });
  return criticalItems;
};

export const getItemsByCategory = (categoryId: string): MLCChecklistItem[] => {
  const category = MLC_CATEGORIES.find(c => c.id === categoryId);
  return category ? category.items : [];
};
